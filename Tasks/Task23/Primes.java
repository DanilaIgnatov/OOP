/*

Создайте функцию, которая проверяет, является ли целое 
число совершенным числом или нет. Совершенное число - это число, 
которое можно записать как сумму его множителей, исключая само число.

Например, 6-это идеальное число, так как 1 + 2 + 3 = 6, 
где 1, 2 и 3-Все коэффициенты 6. Точно так же 28-это 
совершенное число, так как 1 + 2 + 4 + 7 + 14 = 28.


*/

import java.util.Scanner;
import java.math.*;

public class Primes {
	public static void main(String[] args)
	{
		System.out.println("Введите число");
		Scanner in = new Scanner(System.in);
		int n = in.nextInt();

		boolean answer = checkPerfect(n);
		System.out.println(answer);
		


		


	}

public static boolean checkPerfect(int n)
	{
		int dig = 0;
		
		for (int i = 1; i < n; i++)
		{

			if ((n % i) == 0) dig = dig + i;
		}

		if (dig == n) return true;
		else return false;
	}

}